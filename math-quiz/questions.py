def _question_signature(question):
    question_text = " ".join((question.get("question") or "").split())
    options = tuple(" ".join(str(option).split()) for option in (question.get("options") or []))
    return question_text, options


def _dedupe_and_fill_questions(grade, selected, target_count, excluded_ids=None, allow_excluded_fallback=False):
    excluded_ids = set(excluded_ids or [])
    unique = []
    used_ids = set()
    used_signatures = set()

    for question in selected:
        signature = _question_signature(question)
        if question["id"] in used_ids or signature in used_signatures:
            continue
        unique.append(question)
        used_ids.add(question["id"])
        used_signatures.add(signature)
        if len(unique) >= target_count:
            return unique[:target_count]

    question_pool = get_questions_for_grade(grade)
    for question in question_pool:
        if len(unique) >= target_count:
            break
        signature = _question_signature(question)
        if question["id"] in used_ids or signature in used_signatures:
            continue
        if question["id"] in excluded_ids:
            continue
        unique.append(question)
        used_ids.add(question["id"])
        used_signatures.add(signature)

    if allow_excluded_fallback and len(unique) < target_count:
        for question in question_pool:
            if len(unique) >= target_count:
                break
            signature = _question_signature(question)
            if question["id"] in used_ids or signature in used_signatures:
                continue
            unique.append(question)
            used_ids.add(question["id"])
            used_signatures.add(signature)

    return unique[:target_count]


